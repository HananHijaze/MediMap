package com.example.medimap;

public class encodedUser {
    private Integer dietType;
    private Integer bodyType;
    private Integer gender;
    private Integer goal;
    private Integer whereToWorkout;
    private double height;
    private double weight;
    private int age;
    private int mealsPerDay;
    private int snacksPerDay;

    // Getters and Setters for all fields
    public Integer getDietType() {
        return dietType;
    }

    public void setDietType(Integer dietType) {
        this.dietType = dietType;
    }

    public Integer getBodyType() {
        return bodyType;
    }

    public void setBodyType(Integer bodyType) {
        this.bodyType = bodyType;
    }

    public Integer getGender() {
        return gender;
    }

    public void setGender(Integer gender) {
        this.gender = gender;
    }

    public Integer getGoal() {
        return goal;
    }

    public void setGoal(Integer goal) {
        this.goal = goal;
    }

    public Integer getWhereToWorkout() {
        return whereToWorkout;
    }

    public void setWhereToWorkout(Integer whereToWorkout) {
        this.whereToWorkout = whereToWorkout;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getMealsPerDay() {
        return mealsPerDay;
    }

    public void setMealsPerDay(int mealsPerDay) {
        this.mealsPerDay = mealsPerDay;
    }

    public int getSnacksPerDay() {
        return snacksPerDay;
    }

    public void setSnacksPerDay(int snacksPerDay) {
        this.snacksPerDay = snacksPerDay;
    }
}
